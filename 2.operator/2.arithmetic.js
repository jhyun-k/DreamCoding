//산술 연산자 (Arithmetic operators)
/* 더하기
 * - 빼기
 * * 곱하기
 * / 나누기
 * % 나머지값
 * **지수(거듭제곱)
 */

console.log(5+2);
console.log(5-2);
console.log(5*2);
console.log(5/2);
console.log(5%2);
console.log(5**2);

// + 연산자 주의점
let text = '두개의 ' + '문자 연결'
console.log(text); // 문자열 연결 할 때도 쓴다
text = '1' + 1;
console.log(text); // 숫자와 문자열을 더하면 문자열로 변환됨
